%%5参数润滑油模型
%输入产数，经典参数
clear 
clc
W=[0 187 -376 82];
dm=56.6;%单位：mm
D=13.5;%单位：mm
alfa=25;
alfa_o=25*pi/180;%初始接触角/°度
alfa_i=25*pi/180;
omega_i=209;
Q_12=[100 100];
u=[];
u(1)=0.5*W(4)*1e-3*(dm+D*cos(alfa_o))+0.5*D*1e-3*(W(2)*sin(alfa_o)-W(3)*cos(alfa_o));
u(2)=0.5*1e-3*(-W(4)+omega_i)*(dm-D*cos(alfa_i))+0.5*D*1e-3*(W(2)*sin(alfa_i)-W(3)*cos(alfa_i));
Rx=[];
Rx(1)=0.5*D*1e-3*(1+D*cosd(alfa)/dm);
Rx(2)=0.5*D*1e-3*(1-D*cosd(alfa)/dm);
E1=2.075e11;
E=E1/(1-0.3^2);
yita_0=0.033;
k=0.0966;
beta=0.0215;
T1=30;
W1=Q_12./(E.*Rx.^2);
U=yita_0.*u./(2*E.*Rx);
T=T1*sqrt(k*beta*yita_0)./(E.*Rx);
uox=0.5*W(4)*1e-3*(dm+D*cos(alfa_o))+0.5*D*1e-3*(W(3)*cos(alfa_o)-W(2)*sin(alfa_o));
uix=0.5*1e-3*(W(4)-omega_i)*(dm-D*cos(alfa_i))+0.5*D*1e-3*(-W(3)*cos(alfa_i)+W(2)*sin(alfa_i));
s=[];
s(1)=2*abs(uox)/u(1);
s(2)=2*abs(uix)/u(2)
%5参数计算
    WF=7.956015e12.*T.^2+5553.09.*T+1.758455e-6;
    A=-4.793526e-8*W1.^(0.0068361.*abs(WF/W1-1)).*U.^(-0.4047492).*T.^(-0.1833848);
    B=8.37449e-15*W1.^(0.01409715.*abs(WF/W1-1)).*U.^(-0.5868325).*T.^(-0.8173636);
    C=1.180823e-4*W1.^(0.006321.*abs(WF/W1-1)).*U.^(-0.20617).*T.^(-0.3631740);
    D=4.793526e-8*W1.^(0.0068361.*abs(WF/W1-1)).*U.^(-0.4047492).*T.^(-0.1833848);
    mu=(A+B.*s).*exp(-C.*s)+D
    fric=0.1*mu.*Q_12
    F=fric(2)-fric(1)